export function getProjectGallery(slug: string, projectTitle: string): AtlasImage[] | null {
  const items = (GENERATED_PROJECT_GALLERIES as Record<string, ProjectGalleryImage[]>)[slug] || PROJECT_GALLERIES[slug];
  if (!items?.length) return null;

  return items.map((item, index) => ({
    id: `${slug}-curated-${index}`,
    projectId: slug,
    projectTitle,
    type: item.type || 'image',
    src: item.src || item.poster || '',
    poster: item.poster,
    youtubeId: item.youtubeId,
    embedUrl: item.embedUrl,
    externalUrl: item.externalUrl,
    label: item.label,
    caption: item.caption,
    body: item.body,
    role: item.role,
    layout: item.layout,
    emphasis: item.emphasis || (index === 0 ? 'primary' : 'secondary'),
    chapter: item.chapter,
    beat: item.beat,
    relatedSlugs: item.relatedSlugs,
    isPrimary: index === 0,
  }));
}
