import type { AtlasImage } from './atlas';
import { GENERATED_PROJECT_GALLERIES } from './generated/content';
import { getProjectContent } from './projectContent';

export type GalleryRole = 'hero' | 'context' | 'process' | 'system' | 'evidence' | 'coda';
export type GalleryLayout = 'hero' | 'wide' | 'portrait' | 'diagram' | 'compact';
export type GalleryEmphasis = 'primary' | 'secondary' | 'quiet';
export type GallerySlideType = 'image' | 'text' | 'video' | 'audio';

export interface ProjectGalleryImage {
  type?: GallerySlideType;
  src?: string;
  poster?: string;
  youtubeId?: string;
  embedUrl?: string;
  externalUrl?: string;
  label: string;
  caption: string;
  body?: string | string[];
  role: GalleryRole;
  layout: GalleryLayout;
  chapter?: string;
  beat?: string;
  relatedSlugs?: string[];
  emphasis?: GalleryEmphasis;
}

const gallery = (
  slug: string,
  entries: Array<Omit<ProjectGalleryImage, 'relatedSlugs'> & { relatedSlugs?: string[] }>
) => {
  const content = getProjectContent(slug);

  return entries.map((entry, index) => ({
    ...entry,
    chapter: entry.chapter || (index === 0 ? 'Thesis' : 'Evidence'),
    beat: entry.beat || entry.caption,
    relatedSlugs: entry.relatedSlugs || content?.relatedSlugs,
    emphasis: entry.emphasis || (index === 0 ? 'primary' : 'secondary'),
  }));
};

